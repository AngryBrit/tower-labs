const manifest = JSON.parse(
  fs.readFileSync(path.join(root, 'public/research/manifest.json'), 'utf8'),
)

const sections = {}
const items = {}

for (const rel of manifest.sectionFiles) {
  const slug = path.basename(rel, '.json')
  const j = JSON.parse(
    fs.readFileSync(path.join(root, 'public', rel.replace(/^\//, '')), 'utf8'),
  )
  const titleEs = SECTION_TITLE_ES[slug]
  if (titleEs) sections[slug] = titleEs

  const namesEs = ITEM_NAMES_ES[slug]
  if (!namesEs || namesEs.length !== j.items.length) {
    throw new Error(
      `Spanish item list mismatch for ${slug}: expected ${j.items.length}, got ${namesEs?.length ?? 0}`,
    )
  }
  for (let i = 0; i < namesEs.length; i += 1) {
    items[`${slug}:${i}`] = namesEs[i]
  }
}

const out = { sections, items }
const outPath = path.join(root, 'src/i18n/research-overlay.es.json')
fs.writeFileSync(outPath, `${JSON.stringify(out, null, 2)}\n`, 'utf8')
console.log('Wrote', outPath, Object.keys(sections).length, 'sections', Object.keys(items).length, 'items')
